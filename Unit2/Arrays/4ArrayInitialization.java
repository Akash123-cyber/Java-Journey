package Unit2.Arrays;
//For Arrays the Declaration, Memory Allocation and Initialization to certain values can be done together
    
class ArrayInitialization{
    //For Arrays the Declaration, Memory Allocation and Initialization to certain values can be done together
    public static void main(String[] args){
        int[] a = new int[]{1,2,3,4,5};
         //this is for each loop
        for (int i : a) {//this translates into for each i in a
            System.out.println(i);
        }

    }
}

